package customerInfo;

public class CustInformation {
	private String name;
	private int id;
	private int due;
	public String getname() {
		return this.name;		
	}
	public void setname(String name) {
		this.name=name;		
	}
	public int getid() {
		return this.id;		
	}
	public void setid(int id) {
		this.id=id;		
	}
	public int getdue() {
		return this.due;		
	}
	public void setdue(int due) {
		this.due=due;		
	}
}
